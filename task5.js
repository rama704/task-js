function vowelCount(str){
   const vowels = "aeiou";
   let result = {};

   str.toLowerCase().split("").forEach(function(char){
      if(vowels.includes(char)){
         if(result[char]){
            result[char] += 1;
         } else {
            result[char] = 1;
         }
      }
   });
    return result;
}
console.log(vowelCount("Hello World"));
